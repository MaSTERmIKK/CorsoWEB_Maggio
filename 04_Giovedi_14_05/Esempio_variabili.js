// ======================================
// ESEMPI DI VARIABILI IN JAVASCRIPT
// ======================================



// ======================================
// ESEMPIO 1 - Variabile numerica
// ======================================

// Creo una variabile che contiene un numero
let eta = 25;

// Stampo il valore della variabile
console.log(eta);



// ======================================
// ESEMPIO 2 - Variabile testuale
// ======================================

// Creo una variabile stringa
let nome = "Mirko";

// Stampo il contenuto
console.log(nome);



// ======================================
// ESEMPIO 3 - Somma tra variabili
// ======================================

// Due variabili numeriche
let numero1 = 10;
let numero2 = 5;

// Variabile che salva il risultato
let somma = numero1 + numero2;

// Stampa del risultato
console.log(somma);



// ======================================
// ESEMPIO 4 - Concatenazione stringhe
// ======================================

// Variabili testuali
let nomeUtente = "Mario";
let cognomeUtente = "Rossi";

// Unisco le due stringhe
let nomeCompleto = nomeUtente + " " + cognomeUtente;

// Stampo il risultato
console.log(nomeCompleto);



// ======================================
// ESEMPIO 5 - Variabile booleana
// ======================================

// Variabile con valore vero/falso
let maggiorenne = true;

// Stampa del valore
console.log(maggiorenne);



// ======================================
// ESEMPIO 6 - Uso di const
// ======================================

// const crea una variabile costante
const PI = 3.14;

// Stampo il valore
console.log(PI);



// ======================================
// ESEMPIO 7 - Modifica variabile
// ======================================

// Creo una variabile
let punteggio = 50;

// Cambio il valore della variabile
punteggio = 100;

// Stampo il nuovo valore
console.log(punteggio);



// ======================================
// ESEMPIO 8 - Operazioni con variabili
// ======================================

// Variabili numeriche
let prezzo = 20;
let quantita = 3;

// Calcolo del totale
let totale = prezzo * quantita;

// Stampo il risultato
console.log(totale);



// ======================================
// ESEMPIO 9 - Variabile undefined
// ======================================

// Variabile dichiarata ma senza valore
let città;

// Stampo il valore
console.log(città);



// ======================================
// ESEMPIO 10 - Template literal
// ======================================

// Variabili testuali e numeriche
let studente = "Luca";
let voto = 28;

// Creo una frase usando le variabili
let frase = `Lo studente ${studente} ha preso ${voto}`;

// Stampo la frase
console.log(frase);
